

package sorters;

/**<h1>AbstractSorter<h1/>
 * <p>Abstract class for sorting</p>
 *
 * @author Musienko
 */

public abstract class AbstractSorter {
    /**
     * Sorts array
     * @param array - array which will be sorted
     * @return sorted array
     */
    public abstract int[] sort(int[] array);
}
